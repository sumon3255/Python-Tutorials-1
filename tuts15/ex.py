num1 = float(input("Enter first number : "))
operation = input("Enter operator : ")
num2 = float(input("Enter second number : "))
if operation=="*":
    if num1==45 and num2==3:
        print("555")
    else:
        print(num1*num2)
elif operation=="+":
    if num1==45 and num2==3:
        print("77")
    else:
        print(num1+num2)
elif operation=="/":
    if num1==45 and num2==3:
        print("4")
    else:
        print(num1/num2)
else:
    print(num1 - num2)

